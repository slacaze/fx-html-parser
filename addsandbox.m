function addsandbox()
    if ~isempty( ver( 'mcam' ) )
        fx.mcam.command.addsandbox();
    else
        thisPath = fileparts( mfilename( 'fullpath' ) );
        addpath( fullfile(...
            thisPath,...
            'code',...
            'html' ) );
        addpath( fullfile(...
            thisPath,...
            'test' ) );
    end
end